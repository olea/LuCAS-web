<table border="0" cellspacing="0" cellpadding="2" width=600>
<tr bgcolor="#ccffcc">
<td><font size="-1" face="Verdana, Arial, Helvetica">
<b><a name="sobre">Sobre el grupo</a></b>
</font></td>
</tr>
</table>

<p>
CervanTeX es el grupo de usuarios de TeX hispanohablantes, y forma parte
de los grupos locales asociados al TeX User Group.
</p>

<p>
La asociaci&oacute;n busca intercambiar experiencias sobre el sistema inform&aacute;tico 
de composici&oacute;n tipogr&aacute;fica denominado <a 
href="queestex.php">TeX</a>, creado por D. E. Knuth, as&iacute; como 
sobre sus aplicaciones, y promoverlo de forma adecuada en el &aacute;mbito 
hispanohablante (tanto Espa&ntilde;a como Am&eacute;rica).
</p> 

<p>
Estos fines se desarrollan mediante la discusi&oacute;n y la colaboraci&oacute;n en el 
desarrollo de aplicaciones de TeX y programas asociados; la promoci&oacute;n y el 
fomento de publicaciones relacionadas con TeX; la organizaci&oacute;n de cursos, 
congresos y seminarios; y la colaboraci&oacute;n e intercambio con todo tipo de 
sociedades, instituciones y editoriales cient&iacute;ficas.
</p>

<p>TeX se ha convertido un est&aacute;ndard de facto en la composici&oacute;n e
intercambio de textos cient&iacute;ficos a nivel mundial, aunque sus aplicaciones
van m&aacute;s all&aacute; con la posibilidad de componer textos en multitud de lenguas 
y escrituras con una alto grado de refinamiento tipogr&aacute;fico.
</p> 

<table border="0" cellspacing="0" cellpadding="2" width=600>
<tr bgcolor="#ccffcc">
<td><font size="-1" face="Verdana, Arial, Helvetica">
<b><a name="porque">Por qu&eacute; deber&iacute;a asociarme</a></b>
</font></td>
</tr>
</table>

<p>
El desarrollo de nuevas soluciones libres para TeX y LaTeX s&oacute;lo 
puede conseguirse con el debido apoyo humano, material y financiero. 
CervanTeX busca ofrecer el punto de encuentro y el apoyo necesario para que 
tales <a href="proyectos.php">proyectos</a> lleguen a buen fin y sus 
autores puedan desarrollarlos y difundirlos de forma adecuada. De esa 
forma, todos podremos beneficiarnos de ellos.
</p>

<p>
Por otra parte, s&oacute;lo mediante el impulso decidido del uso de TeX
con diversas las <a href="actividades.php">actividades</a>,
tanto en &aacute;mbitos acad&eacute;micos como de artes gr&aacute;ficas, permitir&aacute; que una
herramienta para composici&oacute;n tan potente como TeX tenga un uso tan
generalizado en el &aacute;mbito hispanohablante como ya lo tiene en
otros pa&iacute;ses. Todo ello, teniendo en cuenta que TeX es gratuito.
</p>

<p>
Adem&aacute;s, como tal grupo de TeX, los socios podr&aacute;n disfrutar de 
las <a href="material.php">ventajas</a> que supone pertenecer a CervanTeX, 
como la distribuci&oacute;n de material (CD-ROM, bolet&iacute;n 
<i>TeXemplares</i>) y posibles acuerdos para descuentos en libros, 
programas, congresos, cursos...
</p>

<p>
<a href="inscripcion.php">Quiero asociarme</a>.
</p>

