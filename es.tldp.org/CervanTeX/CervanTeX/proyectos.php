<h2>Proyectos</h2>

<p>
Para proyectos relacionados con castellanizaciones, v&eacute;ase la
<a href="castellano.html">p&aacute;gina</a> 
dedicada espec&iacute;ficamente a ello.
</p>

<p>
<font size="-1" face="Verdana, Arial, Helvetica">
<b><a href="http://w3.mecanica.upm.es/metapost/metagraf.php">
Metagraf</a>
</b></font>
<br />
Desarrollado con la direcci&oacute;n de Santiago Muelas, este entorno 
gr&aacute;fico escrito en Java/Swing
permite dibujar multitud de elementos de forma interactiva y a continuaci&oacute;n 
exportar a MetaPost.
</p>

<p>
<font size="-1" face="Verdana, Arial, Helvetica">
<b><a 
href="ftp://ftp.dante.de/tex-archive/macros/latex/contrib/supported/gloss.zip">
Paquete gloss</a>
</b></font>
<br />
Jos&eacute; Luis D&iacute;az tuvo la idea de usar BibTeX para automatizar
la creaci&oacute;n de glosarios con una base de datos externa. La
actual versi&oacute;n contiene una serie de adiciones gracias a la
difusi&oacute;n de su trabajo en CervanTeX y la colaboraci&oacute;n conseguida. 
</p>


<p>
<font size="-1" face="Verdana, Arial, Helvetica">
<b><a href="http://perso.wanadoo.es/jbezos/archive/lambda.txt">
Soporte multiling&uuml;e para LaTeX con Omega</a>
</b></font>
<br />
A&uacute;n en curso, y presentado parcialmente en Tsukuba (Jap&oacute;n) en
el 2001 por Javier Bezos, el soporte multiling&uuml;e para Omega debe proporcionar
la prosibilidad de componer documentos que incluyan cualquier escritura 
del mundo, perservando las tradiciones tipogr&aacute;ficas locales 
(incluida, naturalmente, la espa&ntilde;ola).
</p>

<p>
<font size="-1" face="Verdana, Arial, Helvetica">
<b><a href="http://tex2pdf.berlios.de/">
tex2pdf</a>
</b></font>
<br />
&Oacute;scar L&oacute;pez es uno de los desarrolladores de este proyecto coordinado
desde Alemania por Steffen Evers. tex2pdf es un gui&oacute;n escrito en Perl
que opera sobre un fichero escrito en LaTeX o en LyX y lo transforma
en formato PDF empleando pdflatex. Tiene soporte para gloss.
</p>

<p>
<font size="-1" face="Verdana, Arial, Helvetica">
<b><a href="ftp://tex.unirioja.es/pub/tex/modelos/curriCICT2000/">
Clase curriCICT2000</a>
</b></font>
<br />
Una clase escrita por Juan Luis Varona que permite preparar un 
curr&iacute;culum vitae normalizado para la Comision Interministerial de Ciencia y 
Tecnologia (Espa&ntilde;a) en el Plan Nacional de I+D+I (2000-2003).
Conrado Badenas ha mejorado el formato y &Oacute;scar L&oacute;pez ha preparado
una plantilla para Lyx.
</p>


