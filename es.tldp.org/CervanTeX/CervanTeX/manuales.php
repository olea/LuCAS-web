<p>
<font size="-1" face="Verdana, Arial, Helvetica">
<b><a href="ftp://ftp.iuma.ulpgc.es/pub/tex/latex2e/doc/ldesc2e/mix/ldesc2e.pdf">
Una descripci&oacute;n de LaTeX</a>
</b></font>
<br />
Tom&aacute;s Bautista ha escrito este documento, que se basa en el
<i>LaTeX-Kurzbeschreibung</i>, de Hubert Partl, Elisabeth Schlegl e 
Irene Hyna, con contenidos ampliados. Esencial para empezar con
LaTeX.
</p>

<p>
<font size="-1" face="Verdana, Arial, Helvetica">
<b><a href="ftp://ftp.dante.de/tex-archive/info/spanish/guia-atx/guia-atx.pdf">
Creación de ficheros LATEX con GNU Emacs</a>
</b></font>
<br />
Manual que aborda un aspecto de la edición de LaTeX que rara vez se trata:
el uso de un procesador para la introducción del texto con su marcado.
La descripción se refiere Emacs, uno de los programas más populares en el
mundo Linux, también disponible en otras plataformas como Windows y Mac.
</p>

<p>
<font size="-1" face="Verdana, Arial, Helvetica">
<b><a href="http://rt0016xp.eresmas.net/lplh/latex-humanidades.pdf">
LaTeX para las humanidades</a>
</b></font>
<br />
Original descripción de LaTeX, concebida como un cuento. 
Evita tecnicismos y está orientado a humanidades. Como
complemento, tiene un conjunto de
<a href="http://rt0016xp.eresmas.net/lplh/lplh.tar.gz">guiones</a>
para el aprendizaje.
</p>


<p>
<font size="-1" face="Verdana, Arial, Helvetica">
<b><a href="http://www.uv.es/~ivorra/Latex/LaTeX.pdf">
Preparaci&oacute;n de texto con LaTeX</a>
</b></font>
<br />
Otro completo manual, aunque de estilo m&aacute;s conciso, esta vez escrito por 
Carlos Ivorra.
</p>


<p>
<font size="-1" face="Verdana, Arial, Helvetica">
<b><a href="http://cipres.cec.uchile.cl/~admcons/manuales/latex.html">
Apuntes - Manual</a>
</b></font>
<br />
Preparado en Chile
</p>




<p>
<font size="-1" face="Verdana, Arial, Helvetica">
<b><a href="http://w3.mecanica.upm.es/~goico/ltxice/">
Curso de TeX / LaTeX</a>
</b></font>
<br />
De la Universidad Polit&eacute;cnica de Madrid, coordinado 
por Jos&eacute; M. Goicolea.
</p>



<p>
<font size="-1" face="Verdana, Arial, Helvetica">
<b><a href="ftp://vtorres.tlc.upv.es/LaTeX/cur2e/">
Curso introductorio a LaTeX</a>
</b></font>
<br />
Transparencias de un  curso preparado por Vicente Torres, de la Universidad 
Polit&eacute;cnica de Valencia.
</p>

<p>
<font size="-1" face="Verdana, Arial, Helvetica">
