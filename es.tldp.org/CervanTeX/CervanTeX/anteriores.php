<h2>Noticias anteriores</h2>


<p>
<font size="-1" face="Verdana, Arial, Helvetica">
<b><a href="pcactual.php">
Sorteo de 30 lotes de CD-ROM con PC Actual (lista de ganadores)</a>
</b>
&nbsp;&nbsp;&nbsp;<small>10/7/2003</small></font> <br />
La revista PC Actual en colaboraci&oacute;n con CervanTeX ha sorteado 30 lotes de
<a href="material.php">CD-ROM</a> (TeXLive y CTAN). La lista de los
30 afortunados ya est&aacute; disponible.
</p>

<p>
<font size="-1" face="Verdana, Arial, Helvetica">
<b><a href="http://www.springer.de/cgi/svcat/search_book.pl?isbn=0-387-95217-9">
Digital Tipography Using LaTeX</a>
</b>
&nbsp;&nbsp;&nbsp;<small>10/5/2003</small></font> <br />
La editoral Springer nos ha remitido este libro de
A. Syropoulos y otros. De nivel b&aacute;sico e intermedio, menciona
multitud de paquetes y trata con cierta extensi&oacute;n Omega y los
documentos en otras lenguas.
<a href="material.php#texemplar"><i>TeXemplares</i></a> incluir&aacute;
una extensa rese&ntilde;a.
</p>

<p>
<font size="-1" face="Verdana, Arial, Helvetica">
<b><a href="http://www.latex.um.es/eguth03/">
EGUTH 2003 (Terceros Encuentros de CervanTeX)</a>
</b>
&nbsp;&nbsp;&nbsp;<small>17/3/2003</small></font> <br />
Los pr&oacute;ximos encuentros de CervanTeX ser&aacute;n en
Murcia (Espa&ntilde;a) los d&iacute;as 25 y 26 de septiembre y est&aacute;n organizados
por el Departamento de Matem&aacute;ticas de la Universidad de Murcia.
</p>


<p>
<font size="-1" face="Verdana, Arial, Helvetica"> <b><a 
href="http://texcalendar.dante.de">Agenda internacional
de TeX</a></b>
&nbsp;&nbsp;&nbsp;<small>7/1/2003</small></font> <br />
Los grupos de TeX alem&aacute;n y neerland&eacute;s han puesto en marcha una agenda
donde poder consultar las convocatorias relacionadas con TeX, aunque
como se acaba de crear, las convocatorias registradas todav&iacute;a son m&aacute;s bien
escasas. CervanTeX se ha encargado de la versi&oacute;n espa&ntilde;ola.
</p>

<p>
<font size="-1" face="Verdana, Arial, Helvetica"> <b><a 
href="http://sunburn.stanford.edu/~knuth/news.html">Knuth actualiza TeX 
y Metafont</a></b>
&nbsp;&nbsp;&nbsp;<small>4/1/2003</small> </b></font> <br />
Los cambios son menores, con correcci&oacute;n de algunos fallos relativamente
infrecuentes. &Eacute;sta la primera actualizaci&oacute;n desde 1995 de TeX, aunque 
conviene recordar que TeX contin&uacute;a evolucionando por otras v&iacute;as (e-TeX,
NTS y, principalmente, <a 
href="http://omega.cse.unsw.edu.au:8080/index.html">Omega</a>).
</p>



<p>
<font size="-1" face="Verdana, Arial, Helvetica"> <b><a 
href="http://w3.mecanica.upm.es/metapost/metagraf.php"> Nueva versi&oacute;n de 
Metagraf</a></b>
&nbsp;&nbsp;&nbsp;<small>21/11/2002</small> </font> <br />
Ahora se pueden hacer dibujos precisos de determinados objetos con 
propiedades geom&eacute;tricas definidas: por ejemplo, una perpendicular a
una recta, tangentes a c&iacute;rculos o elipses, la mediatriz de un segmento...
Esas propiedades se preservan incluso al modificar una parte del dibujo.
</p>


<font size="-1" face="Verdana, Arial, Helvetica"> <b><a 
href="actividades.php#ctan">Distribuci&oacute;n de los CD-ROM a los socios</a>
</b>
&nbsp;&nbsp;&nbsp;<small>21/11/2002</small> </font> <br />
Como en a&ntilde;os anteriores, CervanTeX distribuye entre sus socios los
CD-ROM de TeX-Live y la imagen completa de CTAN. Los socios que
hayan pagado la cuota del 2002 comenzar&aacute;n a recibirlo, junto con
los TeXemplares, en breve.
</p>

<p>
<font size="-1" face="Verdana, Arial, Helvetica">
<b><a href="http://tug.org/tc/devfund/">
El TUG crea el TeX Development Fund</a>
</b>
&nbsp;&nbsp;&nbsp;<small>7/11/2002</small>
</b></font>
<br />
El grupo de usuarios de TeX internacional, al que CervanTeX est&aacute;
afiliado, crea un fondo para ayudar en el desarrollo de proyectos
relacionados con TeX. El presupuesto para el 2003 es de 5000 US$
y se pueden presentar solicitudes hasta el pr&oacute;ximo primero de febrero.
</p>

<p>
<font size="-1" face="Verdana, Arial, Helvetica">
<b><a href="http://omega.enstb.org/eurotex2003">
EuroTeX2003 (24 al 27 de junio)</a></b>
&nbsp;&nbsp;&nbsp;<small>14/10/2002</small></font>
<br />
Con el t&iacute;tulo de <i>Back to Typography</i> el congreso europeo
de TeX se celebrar&aacute; en la Breta&ntilde;a francesa. El plazo para
enviar art&iacute;culos para las ponencias (en cualquier idioma
europeo) acaba en noviembre-diciembre del 2002.
</p>


<p>
<font size="-1" face="Verdana, Arial, Helvetica">
