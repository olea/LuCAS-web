<h2>Material</h2>

<p>
<font size="-2" face="Verdana, Arial, Helvetica">
&Iacute;ndice:
<a href="#cd">CD-ROM / DVD</a>
|
<a href="#texemplar">TeXemplares</a>
|
<a href="#lic">Licencias</a>
</font></p>


<p>
<table border="0" cellspacing="0" cellpadding="2" width=600>
<tr bgcolor="#ccffcc">
<td><font size="-1" face="Verdana, Arial, Helvetica">
<b><a name="cd">CD-ROM y DVD</a></b>
</font></td>
</tr>
</table>
</p>

El DVD y los CD-ROM se han distribuido entre los socios del 2003. 
Los socios del 2004 recibir&aacute;n el material que CervanTeX
distribuya durante el presente a&ntilde;o, cuando se publique.
Aunque las distribuciones como MikTeX o las incluidas en Linux
contienen un buen n&uacute;mero de paquetes, con estos CD-ROM
se dispone tambi&eacute;n de la documentaci&oacute;n m&aacute;s
completa y multitud de utilidades adicionales.

<p>
<table border="0" cellspacing="0" cellpadding="2" width=600>
<tr bgcolor="#e6e6e6">
<td><font size="-1" face="Verdana, Arial, Helvetica">
<b><a name "texlive">TeX Collection 2003</a></b>
</font></td>
</tr>
</table>
</p>
<p>
Este a&ntilde;o, se unen el TeX Live y los CD-ROM de CTAN en un &uacute;nico
paquete que consiste en:
</p>

<p>
<b>live</b><br />
Un DVD con el TeX Live ejecutable y CTAN.
</p>


<p>
<b>inst</b><br />
Un CD-ROM con un instalador del TeX Live.
</p>

<p>
<b>demo</b><br />
Un CD-ROM con una versi&oacute;n resumida del TeX Live (y por
tanto, mucho m&aacute;s que una demo) ejecutable sin instalaci&oacute;n
en Linux, Windows y MacOS X.
</p>

<p>
CTAN es un conjunto de servidores con la mayor colecci&oacute;n de 
<i>software</i> libre para los usuarios de TeX. El DVD contiene
una copia de uno
de esos servidores en la misma forma en que se puede encontrar
por Internet (ftp.dante.de). Entre otros componentes se puede
encontrar: macros y utilidades de BibTeX, MakeIndex y xindy,
tipos y su adaptaci&oacute;n, soporte para lenguas, utilidades para
gr&aacute;ficos, manipulaci&oacute;n de archivos DVI, LaTeX (con todos los
paquetes), Omega, ConTeXt y convertidores de LaTeX a HTML.
</p>

<p>
TeX Live es una selecci&oacute;n con lo mejor de TeX ya preparada para su
uso directo, con una completa serie de tipos
y macros.
Se puede  ejecutar directamente desde el DVD o copiarlo
al disco duro con el CD-ROM de instalaci&oacute;n.
</p>

<p>
<table border="0" cellspacing="0" cellpadding="2" width=600>
<tr bgcolor="#e6e6e6">
<td><font size="-1" face="Verdana, Arial, Helvetica">
<b><a name "ctan">CTAN</a></b>
</font></td>
</tr>
</table>
</p>

En exclusiva para los socios y pensando en los que no disponen
de DVD, tambi&eacute;n se han preparado cuatro CD-ROM con CTAN.

<p>
<table border="0" cellspacing="0" cellpadding="2" width=600>
<tr bgcolor="#ccffcc">
<td><font size="-1" face="Verdana, Arial, Helvetica">
<b><a name ="texemplar">Bolet&iacute;n <i>TeXemplares</i></a></b>
</font></td>
</tr>
</table>
</p>

<p>
El bolet&iacute;n de CervanTeX se llama <i>TeXemplares</i> en alusi&oacute;n
a las <i>Novelas ejemplares</i> (porque Cervantes no s&oacute;lo
escribi&oacute; el <i>Quijote</i>). El nombre tambi&eacute;n juega con
la antigua pronunciaci&oacute;n como <i>j</i>, que sigue viva en M&eacute;xico, de la letra
<i>x</i> y que corresponde a la forma correcta de decir
TeX.
</p>

<p>
<i>TeXemplares</i> quiere tener una periodicidad semestral, y
busca intercambiar ideas y exponer proyectos de una forma que
sea c&oacute;moda para la lectura. La revista est&aacute; completamente
abierta a todo aquel que desee colaborar, sin que sea necesario
que el art&iacute;culo corresponda a un trabajo original: las experiencias
en el uso de TeX y sus programas relacionados, o las rese&ntilde;as y
explicaciones de paquetes, por ejemplo, tambi&eacute;n son bienvenidas.
</p>

<p>
El Editor General de CervanTeX, responsable de la publicaci&oacute;n del
bolet&iacute;n de la asociaci&oacute;n, es <a href="mailto:ema@csn.es">Enrique Mel&eacute;ndez</a>.
</p>

