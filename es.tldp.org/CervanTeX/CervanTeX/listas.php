<td><font size="-1" face="Verdana, Arial, Helvetica">
<b><a></a>Lista <i>sptex</i></b>
</font></td>
</tr>
</table>

<p>
La principal lista de correo para TeX en castellano es <I>es-tex</I>, 
patrocinada por CervanTeX pero ofrecida como servicio gratuito a todo el 
que lo desee, sin que sea necesario ser socio. En ella se pueden comentar y 
discutir aspectos relativos a TeX en castellano y hacer consultar  sobre 
TeX y programas relacionados. <b>Antes de enviar una consulta hay que ir
primero a las <a href="http://corbu.aq.upm.es/~agmartin/latex/FAQ-CervanTeX-main.html">preguntas 
frecuentes</a>.</b> Ocasionalmente, tambi&eacute;n se usa para 
comunicaciones importantes de CervanTeX. 
</p>

<p>
<a href="http://www.rediris.es/list/utilizacion.es.html">
Quiero suscribirme</a> (aqu&iacute; 
tambi&eacute;n se describe su funcionamiento).
<br />
<a href="http://listserv.rediris.es/archives/es-tex.html">Archivo
de mensajes</a>.
</p>

<p>
Cuando se env&iacute;a una consulta a la lista, conviene dar informaci&oacute;n
suficiente acerca del problema. Por ejemplo, en lugar de decir simplemente
&#171;Estoy  intentando generar un PDF, pero no consigo que me salga como
quiero&#187;, es mejor indicar el procedimiento seguido, la distribuci&oacute;n,
qu&eacute; se quiere obtener, qu&eacute; es lo que no sale bien, etc.
De igual modo, cuando se trata de algo de LaTeX conviene dar la
clase y los paquetes usados, las opciones, el resultado que se busca y
el que se obtiene, los posibles mensajes de error, etc.
</p>

<p>
La lista no es muy grande, por lo que un peque&ntilde;o saludo al 
suscribirse, con una breve presentaci&oacute;n, ser&aacute; bienvenido.
</p>

<p><i><b>Sobre la antigua lista Spanish-TeX:</b></i></p>

Para borrarse de la lista, hay que enviar un mensaje a <CODE><A 
HREF="mailto:listserv@eunet.es">listserv@eunet.es</A></CODE> con el 
siguiente texto en el cuerpo del mensaje:

<blockquote>
<CODE>SIGnoff spanish-tex</CODE>
</blockquote>

No hace falta poner nada en el asunto (subject).

<P>
Por favor, no manden peticiones de desuscripci&oacute;n a la propia lista.
</p>

