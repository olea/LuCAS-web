env&iacute;os se har&aacute;n durante el mismo mes de julio
por correo. A los que no les ha tocado,  les deseamos mejor
suerte para una pr&oacute;xima vez. Y a todos, recordamos que tras
CervanTeX se encuentra el trabajo altruista de un buen n&uacute;mero
de personas que necesitan todo el apoyo posible para los
fines que nos hemos propuesto y que resumimos en una frase
de D. E. Knuth, el autor de TeX: <b>crear obras maestras en
el arte de la edici&oacute;n</b>.
