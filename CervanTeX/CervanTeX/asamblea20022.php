<h2>Asamblea de CervanTeX (octubre 2002)</h2><p>Breve resumen de la Asamblea de octubre.(Nota: no son las actas formales.)</p><p>En la Asamblea Extraordinaria de los d&iacute;as 7 y 8 deoctubre CervanTeX, se tomaron las siguientes decisiones:<ul><li>Eugenio Degroote es elegido tesorero.</li><li>Se propone usar las letras t&iacute;picas de la &eacute;poca de Cervantespara el logo de la asociaci&oacute;n y se hacenalgunas propuestas y sugerencias al respecto. Ante la nuevaidea, generalmente bien aceptada, se retrasa la decisi&oacute;n paraelaborarla m&aacute;s; se hacen un par de reservas acerca de queno se use TeX para crear el logo.</li><li>Ante la necesidad de una v&iacute;a de comunicaci&oacute;n entre los socios,se mantendr&aacute; la lista usada para la Asamblea, a la espera de queJoseRa Portillo confirme la posibilidad de usar una nuevalista en su servidor, lo que evitar&iacute;a la molesta publicidadde las listas gratuitas de Internet.</li><li>Se considera que ser&iacute;a buena idea que los socios nos reunamosregularmente de modo informal (cenando, comiendo o tomando unas copas)simplemente para vernos.</li><li>Tambi&eacute;n se propone dar un premio anual a los miembros de sp-texque m&aacute;s ayuda hayan dado. Como no estaba en el orden del d&iacute;a,se pospone para la pr&oacute;xima reuni&oacute;n, y as&iacute; vamos pensando elasunto.</li></ul></tr></table>
