<p>(a) La <b>cuota colectiva</b> está pensada para departamentos y
organismos públicos en general. Por cada lote de material,
se añaden 12 euros. La couta básica de 45 euros sólo incluye un TeXemplares
por número. Los envíos se hacen conjuntamente a una única
dirección postal. No se admiten personas jurídicas como tales
y los miembros del colectivo conservan su individualidad.
</p>

<p>(b) La <b>cuota corporativa</b> está pensada para editoriales y
empresas que trabajan con TeX. Por cada lote de material, se añaden
12 euros. La couta básica de 120 euros sólo incluye un TeXemplares
por número. Los envíos se hacen conjuntamente a una única
dirección postal. Sólo personas jurídicas registradas como tales.
<b>Derechos adicionales:</b> Una página en TeXemplares al año, folletos
(hasta 50 g por sobre) junto con el material enviado a los socios, un
enlace en estas páginas.
</p>

<p>
La f&oacute;rmula m&aacute;s normal en la mayor&iacute;a de los casos es la de
<b><i>individual con material</i></b>. La variante sin material est&aacute;
pensada sobre todo para aquellos que ya son miembros de otro
grupo de usuarios de TeX y no desean recibir el material por
duplicado.
</p>

<p>
Los socios estudiantes debe acreditar esa situaci&oacute;n, y no deber&aacute;n
ejercer ninguna actividad profesional.
</p>

<p>
Las cuotas de los socios deben ser pagadas por adelantado y valen para un 
<b><i>a&ntilde;o natural</i></b> (del uno de enero al treinta y uno de
diciembre). Los nuevos socios pagar&aacute;n por el a&ntilde;o completo, por lo
que puede ser recomendable no asociarse a finales del a&ntilde;o o hacerlo ya
para el siguiente periodo.  El env&iacute;o de material a los socios que no
hayan pagado por adelantado estar&aacute; vinculado a su disponibilidad.
</p>

