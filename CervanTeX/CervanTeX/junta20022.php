<h2>Junta Directiva (noviembre 2002)</h2>

<p>
Breve resumen de la Junta del 28 de noviembre del 2002.
(Nota: no son las actas formales.)
</p>

<p>
<ul>
<li>
Hab&iacute;a dos candidaturas para los encuentros de CervanTeX
del a&ntilde;o 2003, de las universidades de M&aacute;laga y Murcia.
Finalmente se opt&oacute; por la de Murcia, aunque la de M&aacute;laga
sigue abierta a poder organizar una en un futuro. Nuestro
agradecimiento a ambas candidaturas.
</li>
<li>
Se comienza la preparaci&oacute;n de la pr&oacute;xima Asamblea ordinaria,
donde se presentar&aacute;n las cuentas, presupuestos, memoria
y propuesta de cuotas para el 2004.
</li>
<li>
Desgraciadamente, el grupo catal&aacute;n de Tirant-lo-TeX
puede que se disuelva. CervanTeX ha ofrecido su apoyo
para evitarlo, pero en caso de que fuera inevitable,
se aprueba la propuesta de que se pueda formar
eventualmente un grupo espec&iacute;fico de catal&aacute;n dentro
de CervanTeX y que se admitan art&iacute;culos en esa
lengua para TeXemplares (este ofrecimiento  se hace
extensivo a otras lenguas que convivan con el
castellano, naturalmente).
</li>
<li>
Se habla de la posible planificaci&oacute;n de actividades
y m&aacute;s en particular de la posibilidad de participar
en la Semana de la Ciencia de Madrid, para lo que
se comenzar&aacute;n las oportunas gestiones. Tambi&eacute;n se
habla de la posibilidad de ir ofreciendo conferencias
por diferentes centros universitarios de Espa&ntilde;a e
incluso centros de ense&ntilde;anza media. (Si alguien est&aacute;
interesado en organizar una, puede ponerse en contacto
con el presidente.)
</li>
<li>
Se consider&oacute; conveniente enviar un recordatorio a
todos los socios de que los grupos de trabajo est&aacute;n
abiertos a quien deseee participar en ellos.
</li>
<li>
En cuanto a la p&aacute;gina de servicios profesionales de nuestro
sitio, se acuerda que estar&aacute; abierta de forma gratuita a
cualquier socio.
</li>
<li>
Se ha preparado un borrador del logo para su discusi&oacute;n en
la lista interna de CervanTeX. La idea es que sea un borrador
suficiente como para preparar un sello cuanto antes.
</li>
<li>
Se acuerda donar los CD sobrantes de a&ntilde;os anteriores
a una ONG (probablemente Ingenieros sin Fronteras) para
que los distribuya en zonas de Hispanoam&eacute;rica donde el
acceso a Internet sea muy limitado o nulo.
</li>
<li>
Tambi&eacute;n se considera la posibilidad de enlazar desde nuestras
p&aacute;ginas a alguna librer&iacute;a de venta por Internet, para que sea
m&aacute;s f&aacute;cil conseguir material. Se comenzar&aacute;n los contactos
en los pr&oacute;ximos meses con esas librer&iacute;as para llegar a alg&uacute;n
acuerdo que resulte conveniente para ambas partes.
</li>
<li>
Debido al retraso con que se han fijado las cuotas del
2002, y para no obligar a pagar dos a&ntilde;os de una vez, se
prorroga el plazo para pagar el 2003 hasta el primer
trimestre del pr&oacute;ximo a&ntilde;o. Recordamos que seg&uacute;n los estatutos
las cuotas deben pagarse por adelantado.
</li>
<li>
Finalmente, se han sugerido socios honorarios. Se consideraron varios 
nombres a quienes ofrec&eacute;rselo, pero el &uacute;nico que nos ha parecido 
completamente seguro es el del creador de TeX, D. E. Knuth. La lista 
definitiva se llevar&aacute; a la pr&oacute;xima Asamblea para su aprobaci&oacute;n.
</li>
</ul>




