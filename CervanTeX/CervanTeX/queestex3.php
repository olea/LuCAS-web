<p>
Otro concepto clave en el uso de TeX es el de <i>formato</i>. TeX
combina los m&eacute;todos para realizar la composici&oacute;n tipogr&aacute;fica
con un lenguaje de programaci&oacute;n, pero en principio no ofrece m&aacute;s.
Si se limitara a la composici&oacute;n, no ser&iacute;a muy distinto de un
sistema de fotocomposici&oacute;n y habr&iacute;a que indicar expl&iacute;citamente
los blancos, los tipos de letra, etc., incluso si hay estructuras
que se repiten. Sin embargo, el lenguaje de programaci&oacute;n que incorpora
permite automatizar la presentaci&oacute;n de esos elementos estructurales.
As&iacute;, con indicar \section, se pueden tomar las medidas oportunas para
componer una secci&oacute;n con una cierta letra (por ejemplo, negrita a
16 pt), disponerlo de una forma especial (como centrado, en arracada o
al margen) y pasar la informaci&oacute;n oportuna al &iacute;ndice general y a los
folios de cabeza (t&iacute;tulo, n&uacute;mero de secci&oacute;n, p&aacute;gina...).
</p>

<p>
El conjunto de esas utilidades que permiten establecer la 
presentaci&oacute;n del documento a partir de las indicaciones dadas es lo 
que se conoce con el nombre de <i>formato</i>. Por medio de los formatos 
tendr&iacute;amos los equivalentes a las hojas de estilo de otros 
programas, aunque con una capacidad de manipulado muy superior. Los 
formatos m&aacute;s extendidos son los siguientes:
<ul>
  <li><b>Plain.</b> Desarrollado por el propio Knuth con el principal 
  prop&oacute;sito de mostrar lo que se puede conseguir incluso con unos 
  elementos m&iacute;nimos. No fue concebido para un trabajo diario, aunque 
  no son pocos los que lo usan regularmente.
  
  <li><b>LaTeX.</b> Desarrollado paralelamente al propio TeX por Leslie 
  Lamport como un sistema m&aacute;s completo y adecuado para el trabajo 
  diario. LaTeX es el formato m&aacute;s popular y sigue siendo el preferido 
  en muchos contextos, donde no es raro que se identifique TeX. Se 
  puede extender f&aacute;cilmente con la ayuda de paquetes.
  
  <li><b>ConTeXt.</b> M&aacute;s reciente y desarrollado por Hans Hagen, no 
  tiene todas las posibilidades de LaTeX, pero ofrece un sistema coherente 
  m&aacute;s adecuado para publicaciones de contenido general y est&aacute; 
  integrado con utilidades de preimpresi&oacute;n como la imposici&oacute;n.
</ul>
Todas las distribuciones m&aacute;s o menos recientes incluyen estos tres 
formatos.
</p>

