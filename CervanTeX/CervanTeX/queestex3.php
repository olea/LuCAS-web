<p>
Cada distribuci&oacute;n tiene su propio m&eacute;todo de 
instalaci&oacute;n, as&iacute; que nos centraremos en el TeXLive, a modo de 
ejemplo. De entre las plataformas disponibles en el CD-ROM, seguiremos los 
pasos que corresponden a Windows.
</p>

<p align="center">
<img src="tlinstall.gif" alt="instalador" width="542" height="355"  border="1">
</p>

<p>
Cuando el disco se introduce en la computadora, deber&iacute;a arrancarse 
autom&aacute;ticamente. De no ser as&iacute;, basta ir al directorio 
bin/win32 y abrir TeXLive.exe. Una vez que se abre el programa de 
instalaci&oacute;n, hay que ir al men&uacute; Install y ah&iacute; 
seleccionar TeXLive. Se ir&aacute;n pidiendo datos diversos, pero los 
valores predeterminados suelen ser los m&aacute;s adecuados y por tanto 
basta con ir acept&aacute;ndolos. Sin embargo, en una primera prueba tal 
vez sea mejor optar por la instalaci&oacute;n b&aacute;sica en lugar de 
la recomendada, lo que se puede cambiar en el paso correspondiente:
</p>

<p align="center">
<img src="tlscheme.gif" alt="esquema" width="381" height="311" border="1">
</p>

<p>
Tras instalar TeXLive, hay que instalar un editor, como por ejemplo 
WinShell, lo que se hace con el men&uacute; Install &gt; Editors &gt; 
WinShell. Una vez terminado el proceso, ya se puede probar el ejemplo 
m&iacute;nimo descrito en el apartado <a href="queestex2.php">C&oacute;mo 
funciona</a> de esta descripci&oacute;n de TeX.
</p>

