<p>
Para usar TeX, lo primero es hacerse con una <i>distribuci&oacute;n</i>.
Se llama as&iacute; al programa TeX para una cierta plataforma con una
serie de programas y utilidades complementarias. Cada distribuci&oacute;n
incluye unos complementos distintos y por tanto las hay m&aacute;s o menos
completas en funci&oacute;n de su objetivo de ser m&aacute;s b&aacute;sica y reducida o
m&aacute;s avanzada y extensa. Las distribuciones no siempre contienen un
editor espec&iacute;fico de TeX, y en ese caso conviene hacerse con uno. 
</p>

<p>
Las distribuciones m&aacute;s importantes que podemos encontrar son:
<ul>
  <li>Unix: tetex.
  <li>Windows: MikTeX, fptex (versi&oacute;n de tetex para Windows).
  <li>MacOS: OzTeX, CMacTeX (version de tetex para Aqua), tetex (en Darwin).
</ul>
A ellas hay que a&ntilde;adir el CD-ROM TeXLive, que es tetex con multitud
de adiciones para PostScript, editores de TeX... (TeXLive se distribuye
cuando se publica entre los socios que lo hayan solicitado). Adem&aacute;s de 
&eacute;stas, las hay comerciales como Textures, Y&Y, VTeX o TrueTeX.
</p>

<table border="0" cellspacing="0" cellpadding="2" width=600>
<tr bgcolor="#ccffcc">
<td><font size="-1" face="Verdana, Arial, Helvetica">
<b><a name="inst">Instalaci&oacute;n de TeXLive</a></b>
</font></td>
</tr>
