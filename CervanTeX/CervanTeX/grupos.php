<h2>Coordinadores y grupos de trabajo</h2>

<p>
El trabajo en CervanTeX  se organiza en grupos de trabajo, cada uno de los 
cuales funciona de forma aut&oacute;noma, establece sus propios 
m&eacute;todos de trabajo y elige un coordinador de entre sus 
miembros. Todos los socios pueden adscribirse libremente a estos grupos de 
trabajo, para lo cual basta con contactar con los respectivos 
coordinadores.
</p>

<p>
La adscripci&oacute;n a uno o varios de ellos no implica que se tenga que 
desarrollar una actividad concreta: compartir experiencias, aportar 
sugerencias o simplemente echar una mano de cuando en cuando son 
actividades &uacute;tiles que no requieren un gran esfuerzo y siempre resultan 
&uacute;tiles. Toda colaboraci&oacute;n siempre es bienvenida.
</p>

<p>
<i><b>Los coordinadores no atienden consultas</b></i>, pues
no es esa su funci&oacute;n. Para plantear dudas hay que ir a la
<a href="listas.php">lista de discusi&oacute;n en espa&ntilde;ol</a>.
</p>

