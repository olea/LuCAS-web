<h2>Asamblea Ordinaria de CervanTeX (abril 2003)</h2>

<p>
Breve resumen de la Asamblea de abril.
(Nota: no son las actas formales.)
</p>

<p>En la Asamblea Extraordinaria de los d&iacute;as 28 al 30 de
abril, se tomaron las siguientes decisiones:
<ul>
<li>
Roberto Herrero es reelegido en su cargo de Secretario.
</li>
<li>
Se ampl&iacute;a el n&uacute;mero de vocales en la Junta Directiva
a tres, siendo elegido para la nueva plaza Enrique Mel&eacute;ndez.
</li>
<li>La Junta Directiva presenta la <a href="memoria.pdf">
Memoria</a> de actividades y
se informa de un pr&oacute;ximo sorteo con un art&iacute;culo en la
revista PC Actual. Se propone comenzar en la ense&ntilde;anza
secundaria la promoci&oacute;n de TeX y del grupo, para lo cual se
sugiere contactar con asociaciones de profesores.
</li>
<li>
El Tesorero presenta las cuentas y los presupuesto,
informando que la cuenta corriente de CervanTeX se ha
abierto recientemente y que todav&iacute;a no se puede hacer
un presupuesto preciso ni conocer con exactitud los
movimientos habidos. Se presenta un <a href="pres2003.txt">presupuesto</a>
aproximado, que recibe la conformidad de la Asamblea,
y se elaborar&aacute;n unas cuentas m&aacute;s precisas en breve, que
se comunicar&aacute;n a la lista de discusi&oacute;n interna de CervanTeX.
Tambi&eacute;n se recuerda lo importante que es indicar el nombre
en los pagos por tranferencia y se informa de que pronto
ser&aacute; posible el pago con tarjeta VISA.
</li>

<li>Se aprueba sin oposici&oacute;n la propuesta de la Junta de
eliminar las cuotas institucionales para el 2004, que
quedar&iacute;an reemplazadas por las siguientes:
<br />
&nbsp;<br />

<i>Cuota colectiva</i><br />
(Pensada para departamentos y organismos p&uacute;blicos
en general.)<br />

45 euros (cuota b&aacute;sica) + 12 euros por cada lote de
material.<br />

Limitaci&oacute;n:<br />
- La cuota b&aacute;sica s&oacute;lo incluye un TeXemplares
  (por n&uacute;mero).<br />

- El env&iacute;o de material se hace conjuntamente a una
  &uacute;nica direcci&oacute;n postal.<br />

- No se admiten personas jur&iacute;dicas y los miembros
  del colectivo conservan su individualidad.<br />
  
&nbsp;<br />

<i>Cuota corporativa</i><br />

(Pensada para editoriales y empresas dedicadas a TeX.)<br />

120 euros (cuota b&aacute;sica) + 12 euros por cada lote de
material.<br />

Derechos:<br />

- Una p&aacute;gina de publicidad en TeXemplares al a&ntilde;o.<br />

- Inclusi&oacute;n de folletos (menos de 50 g por sobre)
  junto con el material que se env&iacute;a a los socios.<br />

- Un enlace en www.cervantex.org.<br />

Limitaci&oacute;n:<br />

- La cuota b&aacute;sica s&oacute;lo incluye un TeXemplares
  (por n&uacute;mero).<br />

- El env&iacute;o de material se hace conjuntamente a una
  &uacute;nica direcci&oacute;n postal.<br />

- S&oacute;lo personas jur&iacute;dicas legalmente registradas
  como tales.<br />


El resto de las cuotas seguir&aacute;n siendo para el 2004
las mismas que para el 2003.<br />
</li>
<li>
Apenas ha discusi&oacute;n sobre los grupos de trabajo, que
sigue siendo un asunto algo abierto.
</li>
<li>Se acuerda proponer a Donald E. Knuth ser socio honor&iacute;fico
de CervanTeX.
</li>

<li>El presidente informa de la nueva ley sobre asociaciones,
que exige algunos cambios en los actuales estatutos, y propone
un borrador para ser discutido en los pr&oacute;ximos meses antes de
que se celebre la Asamblea donde se vote.</li>

<li>Se acuerda conceder un premio anual a la mejor aportaci&oacute;n en
la lista de discusi&oacute;n sptex. El premio consistir&aacute; en un lote de
material, y la decisi&oacute;n se tomar&aacute; por votaci&oacute;n de los propios
miembros de la lista. Esta propuesta se someti&oacute; a votaci&oacute;n,
quedando aprobada por cinco votos contra cuatro.</li>

</ul>

