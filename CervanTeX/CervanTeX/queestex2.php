<h2>Qu&eacute; es TeX: C&oacute;mo funciona</h2>

<table border=0 cellpadding=3 cellspacing=0>
  <tr>
    <td width=10 ></td>
    <td bgcolor=#ccffcc width=100 nowrap>
    <center>
    <font size=-1><a href="queestex.php">&nbsp;Qu&eacute; es TeX&nbsp;</a></font>
    </center>
    </td>
    <td width=10 ></td>
    <td id=t0 bgcolor=#ffcc99 width=100 nowrap>
    <center>
    <font size=-1><b>&nbsp;C&oacute;mo funciona&nbsp;</b></font>
    </center>
    </td>
    <td width=10 ></td>
    <td bgcolor=#ccffcc width=100 nowrap>
    <center>
    <font size=-1><a href="queestex3.php">&nbsp;C&oacute;mo se usa&nbsp;</a></font>
    </center>
    </td>
  </tr>
</table>
<table width=600 border=0 cellpadding=1 cellspacing=0 bgcolor=#ffcc99>
  <tr>
  </tr>
</table>


<p>
<font size="-2" face="Verdana, Arial, Helvetica">
&Iacute;ndice:
<a href="#entrada">Entrada de texto</a>
|
<a href="#prep">Preparaci&oacute;n con su formato</a>
|
<a href="#pres">Presentaci&oacute;n en pantalla</a>
</font></p>

<p>
Todos los programas para preparar texto (ya sea un programa de proceso, de 
maquetaci&oacute;n o de composici&oacute;n) siguen siempre un proceso de 
tres pasos:

<ul>
  <li>Introducci&oacute;n del texto</li>
  <li>Preparaci&oacute;n con su formato</li>
  <li>Presentaci&oacute;n en pantalla</li>
</ul>
Mientras que los programas llamados WYSIWYG unen el primer paso con el 
&uacute;ltimo y ocultan el segundo (para crear la sensaci&oacute;n de que 
realmente se toca el formato),  TeX deja los tres pasos separados de 
forma que se pueden controlar y automatizar m&aacute;s f&aacute;cilmente; 
de esa forma se puede tener <b>el control que las nuevas tecnolog&iacute;as 
exigen y que de hecho se ha perdido en los programas de maquetaci&oacute;n 
m&aacute;s tradicionales,</b> al tiempo que garantizamos una 
precisi&oacute;n tipogr&aacute;fica insuperada hasta el momento. (Algunos 
programas recientes intentan adaptarse a este nuevo m&eacute;todo de 
trabajo, como en el paquete Frame+SGML, pero el concepto de funcionamiento 
de los programas de maquetaci&oacute;n hace que su adaptaci&oacute;n a este 
sustancial avance no sea f&aacute;cil.) Otra ventaja de este m&eacute;todo
es que un error de TeX no corrompe el archivo original, ya que &eacute;ste
no se toca al componerlo, al contrario que otros sistema que manipulan
un &uacute;nico archivo; eso incrementa la seguridad en su uso y evita
perder tiempo y dinero.
</p>

<p>
La separaci&oacute;n de estos tres pasos es tan estricta en
TeX, que en cada uno de ellos se usa un programa
diferente. Eso nos permite usar justamente aquellos
elementos en cada paso que nos resulte m&aacute;s conveniente para
nuestras necesidades. A continuaci&oacute;n describimos algunos
programas disponibles en cada uno de estos tres grupos.
</p>


<table border="0" cellspacing="0" cellpadding="2" width=600>
<tr bgcolor="#ccffcc">
<td><font size="-1" face="Verdana, Arial, Helvetica">
<b><a name "entrada">Entrada de texto</a></b>
</font></td>
</tr>
</table>

<p>
La entrada de texto, o m&aacute;s precisamente su tecleo (o,
como se dice en Catalu&ntilde;a, su picado), es la funci&oacute;n primordial
de los procesadores de texto. Y s&iacute;, cualquier procesador
que permita guardar como texto puede servir. Sin embargo,
ya hay editores adaptados para la funci&oacute;n, que permiten,
por ejemplo, crear cuadros, definir atajos de teclado,
integrar todos los pasos y sus programas en una &uacute;nica
interfaz gr&aacute;fica, abrir ventanas con botones de s&iacute;mbolos...
</p>

<p>
En Windows, uno de los m&aacute;s populares es WinEdit, pero tambi&eacute;n 
est&aacute;n WinShell, TeXniccenter y TeXShell. En Mac, hasta ahora Alpha 
era el m&aacute;s usado, aunque la llegada de la versi&oacute;n X puede 
cambiar las cosas, con la llegada de utilidades Unix como emacs, o 
interfaces como iTeXMac y TeXShop. En Unix el campo tambi&eacute;n es 
amplio, con vi, emacs y sucesores.
</p>

<p>
<hr>
<table><tr><td>
<img src="teximg9.gif" alt="Ejemplo" width="302" height="229" align="left"
hspace="10" border="1">
<font size="-1" face="Verdana, Arial, Helvetica">
<I>Editor de TeX.</I> En este caso se trata de WinShell, que se puede
instalar opcionalmente con el TeXLive y que se integra autom&aacute;ticamente con
&eacute;l. Aqu&iacute; se muestra con un breve documento de LaTeX.
</font>
</td></tr></table>
<hr>
</p>

<p>
Menci&oacute;n aparte merecen los programas que intentan ofrecer
una interfaz gr&aacute;fica tipo WYSIWYG pero pueden exportar como
archivo TeX para continuar luego con el proceso normal.
No son sistemas WYSIWYG reales porque el resultado final
no siempre concide con el que se ve, pero pueden resultar
m&aacute;s c&oacute;modos. Entre ellos est&aacute; LyX, TeXmacs
o el comercial Scientific Word, aunque en ocasiones pueden resultar
problem&aacute;ticos y no ser completamente compatibles con
TeX (particularmente en el caso de Scientific Word, cuyos
archivos son rechazados en algunas revistas y editoriales).
Incluso MS Word da la posibilidad de guardar como TeX si se
instala una extensi&oacute;n llamada Word2TeX.
</p>


<table border="0" cellspacing="0" cellpadding="2" width=600>
<tr bgcolor="#ccffcc">
<td><font size="-1" face="Verdana, Arial, Helvetica">
<b><a name="prep">Preparaci&oacute;n con su formato</a></b>
</font></td>
</tr>
</table>

<p>
Bien, este es el coraz&oacute;n del sistema y es justamente lo
que hace TeX. Una vez que hemos preparado nuestro archivo
de texto con las marcas de c&oacute;mo debe componerse, TeX se
encarga de producir un archivo con ese texto ya preparado
en lo que se denomina archivo DVI, y que se puede ver en
la forma que veremos m&aacute;s adelante. Obs&eacute;rvese que el
original no se toca, al contrario que los sistemas WYSIWYG
donde el formato s&oacute;lo se puede a&ntilde;adir directamente en el
texto y manualmente.
</p>

<p>
La elecci&oacute;n no est&aacute; restringida a DVI, ya que puede generar un archivo PDF, 
bien directamente con una variante llamada pdftex, bien con conversores de 
DVI a PDF o a PostScript. Actualmente, PDF se ha convertido un est&aacute;ndar de 
hecho para el intercambio de informaci&oacute;n cuando el formato es esencial y 
para los flujos de trabajo en artes gr&aacute;ficas que prescinden de los pasos 
mec&aacute;nicos de anta&ntilde;o (como fotolitos) y se preparan las planchas directamente 
de los archivos (directo a plancha o a imprenta).
</p>

<p>
Todav&iacute;a hay m&aacute;s posibilidades, que son las extensiones
que se han hecho o se est&aacute;n haciendo al programa de TeX en s&iacute;,
para a&ntilde;adirle nuevas funciones, y entre las que destacan
tres: eTeX, NTS y Omega. Mientras que eTeX busca hacer la vida
m&aacute;s f&aacute;cil a los autores de estilos de TeX, NTS (escrito en Java)
quiere servir como medio para probar extensiones a modo de prototipos.
Menci&oacute;n aparte merece Omega, sucesor de TeX a medio plazo y
que incorporar&aacute; ideas tomadas de eTeX y NTS, adem&aacute;s de extender
notablemente sus capacidades tipogr&aacute;ficas, de proceso de datos
y de integraci&oacute;n con XML, Unicode y SVG; de hecho, XML ha tomado
ideas de Omega sobre la forma de tratar ciertos aspectos de documentos
con diversas escrituras.
</p>

<table border="0" cellspacing="0" cellpadding="2" width=600>
<tr bgcolor="#ccffcc">
<td><font size="-1" face="Verdana, Arial, Helvetica">
<b><a name="pres">Presentaci&oacute;n en pantalla</a></b>
</font></td>
</tr>
</table>

<p>
Los archivos DVI necesitan un programa espec&iacute;fico para
poder verse en las pantallas, pero casi todas las distribuciones
de TeX incluyen alguno.
</p>

<p>
<hr>
<table><tr><td>
<img src="teximg10.gif" alt="Ejemplo" width="339" height="257"
align="left" hspace="10" border="1">
<font size="-1" face="Verdana, Arial, Helvetica">
<I>Visor de DVI.</I> De nuevo es el instalado por TeXLive. Tras procesar el
documento con el bot&oacute;n &#171;LaTeX&#187; de WinShell, basta con pulsar el que indica
&#171;DVI&#187; para ver el resultado.
</font>
</td></tr></table>
<hr>
</p>

<p>
Si el formato final es PostScript o PDF, el archivo se
podr&aacute; ver en cualquier visor para esos formatos. Para
PDF se puede usar, por ejemplo, Acrobat Reader, mientras
que para PostScript vale cualquier programa capaz de importarlo
adecuadamente. El programa gratuito Ghostscript, disponible
en todas las plataformas, permite ver PostScript y PDF.
Como ya se ha dicho, esos archivos son la base
del trabajo en imprenta para el directo a plancha
(con los programas de imposici&oacute;n correspondientes), pero
PDF tambi&eacute;n puede servir para Internet, ya que TeX permite
crear los marcadores y los enlaces para las remisiones (o referencias
cruzadas) de forma autom&aacute;tica.
</p>

<p>
Tanto en un caso como en otro, lo que se ve en la pantalla es
<b>exactamente</b> WYSIWYG (siglas inglesas que significan &#171;lo que
se ve es lo que se obtiene&#187;). Los mal llamados programa WYSIWYG
presentan peque&ntilde;as variaciones entre lo que se ve en pantalla
y lo que se obtiene impreso debido a que integran visualmente
dos pasos tan conceptualmente distintos como proceso y
visualizaci&oacute;n, lo que no siempre es completamente factible.
</p>

