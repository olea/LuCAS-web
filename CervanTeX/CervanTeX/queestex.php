    align="left" hspace="10" border="1"> <font size="-1" face="Verdana, Arial, Helvetica"> 
    <I>TeX sigue evolucionando.</I> El sistema Omega lee archivos con codificaci&oacute;n 
    Unicode y los representa adecuadamente, con un control tipogr&aacute;fico absoluto 
    sobre las caracter&iacute;sticas propias de otras escrituras. XSL-Formatting 
    Objects ha incorporado ideas procedentes de Omega.
    </font> </td>
  </tr> 
</table>
