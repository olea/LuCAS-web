<h2>Qu&eacute; es TeX</h2>

<table border=0 cellpadding=3 cellspacing=0>
  <tr>
    <td width=10 ></td>
    <td id=t0 bgcolor=#ffcc99 width=100 nowrap>
    <center>
    <font size=-1><b>&nbsp;Qu&eacute; es TeX&nbsp;</b></font>
    </center>
    </td>
    <td width=10 ></td>
    <td bgcolor=#ccffcc width=100 nowrap>
    <center>
    <font size=-1><a href="queestex2.php">&nbsp;C&oacute;mo funciona&nbsp;</a></font>
    </center>
    </td>
    <td width=10 ></td>
    <td bgcolor=#ccffcc width=100 nowrap>
    <center>
    <font size=-1><a href="queestex3.php">&nbsp;C&oacute;mo se usa&nbsp;</a></font>
    </center>
    </td>
  </tr>
</table>
<table width=600 border=0 cellpadding=1 cellspacing=0 bgcolor=#ffcc99>
  <tr>
  </tr>
</table>


<p>
<font size="-2" face="Verdana, Arial, Helvetica">
&Iacute;ndice:
<a href="#noproc">No es un procesador</a>
|
<a href="#nomaq">No es un maquetador</a>
|
<a href="#entonces">Entonces, &#191;qu&eacute; es?</a>
</font></p>

<p>
TeX es un sistema de <b>composici&oacute;n tipogr&aacute;fica de textos</b> 
de alta calidad y refinamiento, y con funciones avanzadas de 
automatizaci&oacute;n. TeX en s&iacute; mismo es un completo lenguaje de 
programaci&oacute;n que permite el marcado l&oacute;gico de documentos de 
forma que el formato se pueda generar autom&aacute;ticamente.
</p>

<p>
TeX fue desarrollado por Donald Knuth en el a&ntilde;o 1978, por 
petici&oacute;n de la American Mathematical Society, para poder componer 
texto y f&oacute;rmulas matem&aacute;ticas con la calidad de los sistemas 
de fotocomposici&oacute;n de entonces, pero sin el penoso esfuerzo que esos 
sistemas requer&iacute;an.
</p>

<p>
<hr>
<table>
  <tr>
    <td> <img src="teximg1.gif" alt="Ejemplo" width="268" height="234" 
    align="left" hspace="10" border="1"> <font size="-1" face="Verdana, Arial, Helvetica"> 
    <I>Composici&oacute;n de matem&aacute;ticas.</I> &Eacute;ste es el origen de TeX y donde todav&iacute;a sigue 
    siendo insustituible, sin que haya ning&uacute;n sistema de maquetaci&oacute;n que tan 
    siquiera se aproxime a sus resultados. El est&aacute;ndar MathML, para representar 
    matem&aacute;ticas con XML, est&aacute; basado en TeX.
    </font> </td>
  </tr> 
</table>
<hr>
</p>


<p>
Knuth, junto con las dos entidades que prestaron apoyo al proyecto (la AMS 
y Addison-Wesley), decidieron poner el programa a disposici&oacute;n de todo 
aquel que quisiera usarlo, sin ning&uacute;n tipo de restricciones. Sin 
embargo, aunque su gratuidad haya contribuido a su difusi&oacute;n y a que 
se haya convertido en un est&aacute;ndar de facto para el intercambio a 
nivel mundial de informaci&oacute;n cient&iacute;fica, las razones de su 
&eacute;xito no hay que buscarlas en ese hecho sino en la alta calidad de 
los resultados.
</p>

<p>
TeX est&aacute; disponible en casi cualquier plataforma existente, y sus 
archivos se pueden intercambiar con relativa facilidad entre ellas. La 
combinaci&oacute;n de estas cuatro caracteristicas de calidad, gratuidad, 
disponibilidad y compatibilidad han sido, en definitiva, las claves para su 
amplia difusi&oacute;n.
</p>

<p>
<hr>
<table><tr><td>
<img src="teximg2.gif" alt="Ejemplo" width="205" height="154" align="left" 
hspace="10" border="1">
<font size="-1" face="Verdana, Arial, Helvetica">
<I>De la idea a la 
imprenta.</I> TeX cubre todo el proceso en la creaci&oacute;n de un libro, 
desde la escritura por el autor, liberado de la tarea del formato 
(<I>izquierda</I>), pasando por la composici&oacute;n (<I>centro</I>), 
hasta la imposici&oacute;n (<I>derecha</I>).  Los resultados se muestran en 
formato PDF, que tambi&eacute;n ha tomado ideas de TeX.
</font>
</td></tr></table>
<hr>
</p>

<table border="0" cellspacing="0" cellpadding="2" width=600>
<tr bgcolor="#ccffcc">
<td><font size="-1" face="Verdana, Arial, Helvetica">
<b><a name "noproc">TeX no es un procesador de textos</a></b>
</font></td>
</tr>
</table>

<p>
El objetivo de los procesadores de texto es la introducci&oacute;n (es 
decir, el tecleo) y preparaci&oacute;n de texto. Programas como WordStar, 
WordPerfect, Word... son la l&oacute;gica continuaci&oacute;n de la 
m&aacute;quina de escribir, aunque todos ellos ofrecen 
caracter&iacute;sticas adicionales que los hacen &uacute;tiles para 
bastantes tareas, siempre que no est&aacute;n relacionadas con la 
composici&oacute;n profesional. TeX no es un procesador de texto, y de 
hecho hace falta un programa externo para el propio tecleo del documento 
que va a ser compuesto.
</p>

<table border="0" cellspacing="0" cellpadding="2" width=600>
<tr bgcolor="#ccffcc">
<td><font size="-1" face="Verdana, Arial, Helvetica">
<b><a name="nomaq">TeX no es un programa de maquetaci&oacute;n</a></b>
</font></td>
</tr>
</table>
<h3></h3>

<p>
Los programas de maquetaci&oacute;n permiten la distribuci&oacute;n de 
texto en la p&aacute;gina seg&uacute;n una cierta maqueta o diagrama. Su 
principal uso es sobre todo en peri&oacute;dicos y revistas ilustradas, que 
hasta la aparici&oacute;n del Macintosh en 1984 y programas como PageMaker, 
Ventura y QuarkXPress ten&iacute;an que hacerse por medios completamente 
artesanales o de un alt&iacute;simo coste. Sin embargo, TeX no esta dedicado a la maquetaci&oacute;n (al 
menos de esta forma) y su uso en los contextos donde PageMaker 
revolucion&oacute; la compaginaci&oacute;n es bastante limitado. Los 
programas de maquetaci&oacute;n  prestan relativamente poca atenci&oacute;n 
a la composici&oacute;n del texto (es decir, la forma en que cada letra se 
coloca con relaci&oacute;n a las dem&aacute;s, y la forma en que se 
componen las l&iacute;neas), que es uno de los principales objetivos de 
TeX.
</p>

<table border="0" cellspacing="0" cellpadding="2" width=600>
<tr bgcolor="#ccffcc">
<td><font size="-1" face="Verdana, Arial, Helvetica">
<b><a name="entonces">Entonces, &#191;qu&eacute; es TeX?</a></b>
</font></td>
</tr>
</table>

<p>
En realidad, TeX se entronca m&aacute;s con las t&eacute;cnicas de 
fotocomposici&oacute;n de Compugraphic, Digiset o Fototronic, donde el 
&eacute;nfasis se pone en la forma en que el texto se distribuye en l&iacute;neas. 
Al igual que los programas de proceso de texto han incorporado funciones 
que hacen de las m&aacute;quinas de escribir algo del pasado, TeX ha incorporado 
una serie de funciones que permiten la calidad de composici&oacute;n de los 
sistemas de fotocomposici&oacute;n tradicionales al tiempo que extiende sus  
capacidades en tal grado que se esta convirtiendo en una pieza fundamental  
para tecnolog&iacute;as recientes como XML y permite avanzar en el desarrollo de 
nuevos sistemas en el nuevo milenio.
<b>Pero al contrario que los sistemas de 
fotocomposici&oacute;n, cuyo uso era extremadamente complicado, TeX pone al 
alcance de cualquiera con un esfuerzo relativamente peque&ntilde;o todas 
sus posibilidades (en realidad m&aacute;s, al poder automatizarse).
</b>
</p>

<p>
<hr>
<table>
  <tr>
    <td> <img src="teximg3.gif" alt="Ejemplo" width="205" height="154" 
    align="left" hspace="10" border="1"> <font size="-1" face="Verdana, Arial, Helvetica"> 
    <I>Integraci&oacute;n con PostScript.</I> Los gr&aacute;ficos no son adiciones a la p&aacute;gina sino 
    que pueden ser parte integrante del texto en s&iacute; y permitir as&iacute; efectos 
    interesantes. El &oacute;valo del pie y la flecha de enlace con la nota marginal 
    no est&aacute;n dibujados a mano, sino que el texto est&aacute; marcado para se tracen. 
    </font> </td>
  </tr> 
</table>
<hr>
</p>

<p>
De esta forma, al permitir el acceso de todos a unas prestaciones tal 
altas, tanto autores como empresas de artes gr&aacute;ficas pueden sacar 
ventaja de TeX: los autores, porque ya no necesitan acudir a una empresa 
de fotocomposici&oacute;n para preparar un libro, y las artes 
gr&aacute;ficas, al poder prescindir, precisamente, de dichos sistemas sin 
renunciar a la calidad.  M&aacute;s aun, el trabajo combinado de 
autor y editorial permite un proceso m&aacute;s r&aacute;pido y 
econ&oacute;mico por medio de plantillas de TeX que produzcan los 
resultados adaptados al estilo de la editorial, al poder disponer ambos
de las herramientas necesarias.
</p>

<p>
