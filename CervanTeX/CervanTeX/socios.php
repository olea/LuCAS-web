</p>

Esta lista esta reservada a los socios de CervanTeX y su
funci&oacute;n es el intercambio de ideas, sugerencias, etc. relacionadas 
exclusivamente con la marcha del grupo. <b>Esta lista no es para
consultas sobre TeX.</b> Al tratarse de una
lista restringida, la suscripci&oacute;n pasar&aacute; antes por la
autorizaci&oacute;n del administrador, por lo que la entrada
definitiva puede tardar un poco.

<b>Por problemas técnicos del servidor donde se alojaba
esta lista, se ha cerrado temporalmente. Confiamos en
encontrar una alternativa en breve.</b>


