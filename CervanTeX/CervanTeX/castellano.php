<b><a name="distribuciones"></a>Distribuciones</b>
</font></td>
</tr>
</table>

<p>
<font size="-1" face="Verdana, Arial, Helvetica"> <b><a 
href="ftp://tex.unirioja.es/pub/tex/oztex40/">OzTeX 4.0</a>
</b></font>
<br />
OzteX 4.0 con babel y cortes en espa&ntilde;ol junto con algunas utilidades para 
Mac relacionadas con TeX en el servidor ftp de TeX en espa&ntilde;ol en la 
Universidad de La Rioja (Espa&ntilde;a). Incluye AmS-LaTeX 2.0.

<p>
<font size="-1" face="Verdana, Arial, Helvetica">
<b><a href="ftp://tex.unirioja.es/pub/tex/textures-latex/">
Textures</a>
</b></font>
<br />
Formato LaTeX2e (incluye AmS-LaTeX 2.0) para Textures con babel y cortes en 
espa&ntilde;ol tambi&eacute;n en el servidor ftp de TeX en espa&ntilde;ol en la Universidad de 
La Rioja (Espa&ntilde;a).
</p>


