</font></p>

<p>
<table border="0" cellspacing="0" cellpadding="2" width=600>
<tr bgcolor="#ccffcc">
<td><font size="-1" face="Verdana, Arial, Helvetica">
<b><a name = "tex"></a>TeX, LaTeX...</b>
</font></td>
</tr>
</table>
</p>

<p>
<font size="-1" face="Verdana, Arial, Helvetica">
<b><a href="spanish.dtx">
Spanish 4.2b para babel</a>
</b></font>
<br />
Babel es un paquete para la adaptaci&oacute;n de TeX a diversos idiomas,
entre ellos el espa&ntilde;ol. La &uacute;ltima versi&oacute;n de spanish suele incorporarse
a babel con cierto retraso, pero se puede obtener aqu&iacute;. Basta con
descargarlo, reemplazar el spanish de babel con el nuevo y regenerar
babel (o s&oacute;lo spanish, corriendo spanish.ins). La extensa
documentaci&oacute;n
en castellano se obtiene componiendo spanish.dtx directamente.
</p>



<p>
<font size="-1" face="Verdana, Arial, Helvetica">
<b><a href="ftp://ftp.dante.de/tex-archive/language/spanish/hyphen.zip">
Patrones para guionado de palabras</a>
</b></font>
<br />
De los muchos existentes (hay como m&iacute;nimo seis) s&oacute;lo los de Julio S&aacute;nchez 
y los de Francesc Carmona (incluidos en TeXLive) son recomendables. Actualmente los de Julio 
S&aacute;nchez son los recomendados, pero se est&aacute; trabajando en unificarlos, 
tomando como base sobre todo los de Francesc Carmona.
</p>

<table border="0" cellspacing="0" cellpadding="2" width=600>
<tr bgcolor="#ccffcc">
<td><font size="-1" face="Verdana, Arial, Helvetica">
<b><a name="bibtex"></a>BibTeX</b>
</font></td>
</tr>
</table>



<p>
<font size="-1" face="Verdana, Arial, Helvetica">
<b><a href="ftp://ftp.cma.ulpgc.es/pub/tex/bibtex/abstyles">
Adaptable family</a>
</b></font>
<br />
Una serie de estilos de BibTeX adaptados para el espa&ntilde;ol por
Tom&aacute;s Bautista. Se pueden personalizar f&aacute;cilmente.
</p>


<p>
<font size="-1" face="Verdana, Arial, Helvetica">
<b><a 
href="ftp://ftp.dante.de/tex-archive/macros/latex/contrib/supported/custom-bib.zip">
custom-bib</a>
</b></font>
<br />
Un programa escrito en TeX para crear archivos bst personalizados
a partir de una serie de preguntas y sin necesidad de conocer el
lenguage propio de BibTeX. El m&oacute;dulo para castellano ha sido preparado
por Tom&aacute;s Bautista.
</p>

<table border="0" cellspacing="0" cellpadding="2" width=600>
<tr bgcolor="#ccffcc">
<td><font size="-1" face="Verdana, Arial, Helvetica">
<b><a name="indices"></a>&Iacute;ndices alfab&eacute;ticos</b>
</font></td>
</tr>
</table>

<p>
<font size="-1" face="Verdana, Arial, Helvetica">
<b><a 
href="ftp://ftp.dante.de/tex-archive/macros/latex/contrib/supported/bezos.zip">
esindex</a>
</b></font>
<br />
Paquete de LaTeX que permite introducir f&aacute;cilmente las entradas
del &iacute;ndice alfab&eacute;tico de forma que queden ordenadas correctamente sin
recurrir a @.
</p>

<p>
<font size="-1" face="Verdana, Arial, Helvetica">
<b><a href="http://www.aq.upm.es/~agmartin/linux/store/xindy-2.0.tgz">
xindy</a>
</b></font>
<br />
Agust&iacute;n Mart&iacute;n Domingo ha preparado dos ficheros para espa&ntilde;olizar XINDY, un 
programa alternativo a makeindex para la creaci&oacute;n de &iacute;ndices 
en documentos TeX (libc5 y glibc2.1). [Dos archivos: descargar el segundo 
<a 
href="http://www.aq.upm.es/~agmartin/linux/store/xindyg21-2.0.tgz">aqu&iacute;</a>]
</p>


<table border="0" cellspacing="0" cellpadding="2" width=600>
<tr bgcolor="#ccffcc">
<td><font size="-1" face="Verdana, Arial, Helvetica">
<b><a name="diccionarios"></a>Diccionarios y gram&aacute;tica</b>
</font></td>
</tr>
</table>

<p>
<font size="-1" face="Verdana, Arial, Helvetica">
<b><a href="http://www.datsi.fi.upm.es/~coes/espa~nol.tar.gz">
ispell</a>
</b></font>
<br />
Santiago Rodr&iacute;guez y Jes&uacute;s Carretero ha preparado un fichero de afijos y 
un diccionario para ispell. Hay un port para emx en fase beta (que funciona
o debe funcionar tanto en MS-DOS (con 386 o superior) como en OS/2 2.x
o en OS/2 Warp)
</p>

<p>
<font size="-1" face="Verdana, Arial, Helvetica"> <b><a 
href="http://theory.uwinnipeg.ca/scripts/CTAN/systems/mac/support/excalibur/"> Excalibur</a> 
</b></font> <br />
Diccionario spanish, de Juan L. Varona, para Excalibur (MacOS)
</p>

<p>
<font size="-1" face="Verdana, Arial, Helvetica">
<b><a href="http://csg.uwaterloo.ca/~dmg/comp-jugador/index.html">
compjugador</a>
</b></font>
<br />
Daniel M. Germ&aacute;n ha escrito esta utilidad para Unix
(de hecho compilable en cualquier arquitectura) para conjugar
verbos del espa&ntilde;ol. 
</p>

